echo "✓ Duration metrics (~90 seconds total)"
echo "✓ Screenshots (30+ captured on failures)"
echo "✓ Page source HTML snapshots"
echo "✓ Timeline of test execution"
echo "✓ Feature-based organization"
echo "✓ Failure analysis and categorization"
echo ""

echo "════════════════════════════════════════════════════════════════"
echo "  RE-RUN TESTS (if needed)"
echo "════════════════════════════════════════════════════════════════"
echo ""
echo "To run tests again and generate a new report:"
echo ""
echo "  cd '$PROJECT_DIR'"
echo "  pytest tests/ \\"
echo "    --browser=chromium \\"
echo "    --base-url=https://automationexercise.com \\"
echo "    --alluredir=allure-results -v"
echo ""

