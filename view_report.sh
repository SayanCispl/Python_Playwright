#!/bin/bash
# Quick commands to view Allure Report

echo "════════════════════════════════════════════════════════════════"
echo "  Python Playwright Allure Report - View Options"
echo "════════════════════════════════════════════════════════════════"
echo ""

PROJECT_DIR="/Users/codeclouds-sayan/Projects/Python_Playwright"
REPORT_HTML="$PROJECT_DIR/allure-report/index.html"
RESULTS_DIR="$PROJECT_DIR/allure-results"

echo "📂 PROJECT LOCATION:"
echo "   $PROJECT_DIR"
echo ""

echo "════════════════════════════════════════════════════════════════"
echo "  OPTION 1: Open Report in Browser (Quickest)"
echo "════════════════════════════════════════════════════════════════"
echo ""
echo "Command:"
echo "  open '$REPORT_HTML'"
echo ""
echo "Or click here to open:"
echo "  file://$REPORT_HTML"
echo ""

echo "════════════════════════════════════════════════════════════════"
echo "  OPTION 2: Run Allure Server (Recommended)"
echo "════════════════════════════════════════════════════════════════"
echo ""
echo "Commands:"
echo "  cd '$PROJECT_DIR'"
echo "  allure serve allure-results"
echo ""
echo "This will:"
echo "  ✓ Start a local web server"
echo "  ✓ Open report in your default browser"
echo "  ✓ Allow real-time updates if tests are re-run"
echo ""

echo "════════════════════════════════════════════════════════════════"
echo "  OPTION 3: Verify Report Exists"
echo "════════════════════════════════════════════════════════════════"
echo ""
echo "Check if report directory exists:"
if [ -d "$PROJECT_DIR/allure-report" ]; then
    echo "  ✅ Report directory found!"
    echo "     Location: $PROJECT_DIR/allure-report/"
    echo ""
    echo "Report contents:"
    ls -la "$PROJECT_DIR/allure-report/" | grep -E "^d|\.html|\.js|\.css" | head -10
    echo ""
else
    echo "  ❌ Report directory not found"
    echo "     Run: allure generate allure-results --clean -o allure-report"
fi
echo ""

echo "════════════════════════════════════════════════════════════════"
echo "  OPTION 4: Check Test Results"
echo "════════════════════════════════════════════════════════════════"
echo ""
echo "Total test result files:"
find "$RESULTS_DIR" -name "*result.json" 2>/dev/null | wc -l
echo ""
echo "Test attachments:"
echo "  Screenshots: $(find "$RESULTS_DIR" -name "*.png" 2>/dev/null | wc -l)"
echo "  HTML files:  $(find "$RESULTS_DIR" -name "*.html" 2>/dev/null | wc -l)"
echo ""

echo "════════════════════════════════════════════════════════════════"
echo "  WHAT'S IN THE REPORT"
echo "════════════════════════════════════════════════════════════════"
echo ""
echo "✓ Test execution statistics (9 tests total)"
